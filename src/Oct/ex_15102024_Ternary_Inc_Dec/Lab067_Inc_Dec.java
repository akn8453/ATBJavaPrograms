package Oct.ex_15102024_Ternary_Inc_Dec;

public class Lab067_Inc_Dec {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + a); //  A  -> a++  , B  - a
        // A+B
        // A -> a++ -> 10 ->  a = 11
        // B - a -> 11
        // A +B -> 10+11
    }
}
