package Task.Task_14102024;

// Converting double in to int type.

public class Task_14_TypeCasting_Ex_2 {
    public static void main(String[] args) {
        // create a simple double variable
         double num1 = 14.87;
        System.out.println("The value of num1 is " + num1);

        //Converting double into a int data type
        int num2  = (int) num1;
        System.out.println("The value of num2 is "+ num2);
    }
}
