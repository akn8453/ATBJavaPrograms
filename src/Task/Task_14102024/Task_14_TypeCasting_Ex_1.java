package Task.Task_14102024;

// Converting Int type in to double

public class Task_14_TypeCasting_Ex_1 {
    public static void main(String[] args) {
        // create a simple int variable
        int a = 10;
        System.out.println("The value of a is " + a);

        //Converting int into a double data type
        double num = a;
        System.out.println("The value of double is "+ num);
    }
}
