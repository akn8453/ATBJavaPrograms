package Task.Task_11102024;

public class Task_11_Ubary_Op {
    public static void main(String[] args) {
        int a = 10;
        int b = 9;

        System.out.println("Post-Increment of a  =  " + (a++));
        System.out.println("Pre-Increment of a  =  " + (++a));
        System.out.println("Post-Increment of b =  " + (b++));
        System.out.println("Pre-Increment of b =  " + (++b));
        System.out.println("Post-Decrement of a  =  " + (a--));
        System.out.println("Pre-Decrement of a  =  " + (--a));
        System.out.println("Post-Decrement of b =  " + (b--));
        System.out.println("Pre-Decrement of b =  " + (--b));


    }
}
